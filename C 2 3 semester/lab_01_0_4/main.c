/**
 *   Бутылка воды стоит 45 копеек. Пустые бутылки сдаются по 20 копеек, и на полученные деньги опять
 *   покупается вода.Какое наибольшее количество бутылок воды можно купить,
 *   имея некоторую сумму денег S копеек.
 */

#include <stdio.h>

int main()
{
    int s;
    scanf("%d", &s);
    s = (s - 20) / 25;
    printf("%d ", s);
    return 0;
}
